DROP TABLE IF EXISTS CONTACTINFO;
DROP TABLE IF EXISTS PERSON_INFO;

CREATE TABLE IF NOT EXISTS BASEOBJECT(ID INT NOT NULL AUTO_INCREMENT, PRIMARY KEY(ID));

CREATE TABLE IF NOT EXISTS CONTACTINFO(
      ID INT NOT NULL AUTO_INCREMENT, 
      PERSON_CONTACT_ADDR VARCHAR(60) NOT NULL,
      PERSON_CONTACT_NO VARCHAR(40) NOT NULL,
      PRIMARY KEY(ID)
      );

CREATE TABLE IF NOT EXISTS PERSON_INFO(
      ID INT NOT NULL AUTO_INCREMENT, 
      PERSON_NAME VARCHAR(60) NOT NULL,
      PERSON_DOB DATE,PRIMARY KEY(ID)
      );